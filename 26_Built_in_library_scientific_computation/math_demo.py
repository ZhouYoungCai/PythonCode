import math

print(math.pi)  #描述圆周率，值为3.1415926535.

print(math.e)  #自然对数，值为2.7182818..

print(math.inf)  #正无穷大

print(-math.inf)  #负无穷-math.inf

print(math.nan)  #非数字NAN(Not a Number)

print(math.ceil(4.3))   # 向上取整  >4.3 最小的整数  5

print(math.ceil(-4.3))  # 向上取整  >-4.3 最小的整数  -4

print(math.ceil(-0.1))  # 向上取整  >-0.1 最小的整数  0

print(math.floor(4.3))  # 向下取整  <4.3 最大的整数 4

print(math.floor(-4.3))  # 向下取整  <-4.3 最大的整数 -5

print(math.pow(2, 10))   #返回x的y次幂，2的10次幂，1024.0

print(math.sqrt(16))  #返回x的平方根，16的平方根4

print(math.pow((1.0 + 0.01), 365))  #1.01的365次幂，37.78343433288728

print(math.pow((1.0 - 0.01), 365))  #0.99的365次幂，0.025517964452291125